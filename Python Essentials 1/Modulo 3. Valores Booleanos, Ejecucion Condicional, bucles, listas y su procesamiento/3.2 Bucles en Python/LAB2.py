import time

# Escribe un bucle for que cuente hasta cinco.
for i in range(1, 6):
    # Cuerpo del bucle: imprime el número de iteración del bucle y la palabra "Mississippi".
    print(str(i), "Mississippi")
    # Cuerpo del bucle, emplea : time.sleep(1)
    time.sleep(1)
